package primitivecasting;

public class narrowing {
	
	// Narrowing -->  Converting bigger data type into smaller data type (explicitly) means our own .
	

	public static void main(String[] args) {
		
		
		double i = 12.34;
		
		int j = (int)i;
		
		System.out.println(j);
		
		int z = (int) 45.02;
		
		System.out.println(z);
		
		
		int p = 97;
		
		char c = (char)p;
		
		System.out.println(c);
		System.out.println((int)970.56);
		System.out.println(10.0+"A");
		System.out.println((double)45);
		
	}
	
		
	
	

}
